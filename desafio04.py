v =input('Digite algo:')
print('O tipo primitivo desse valor é ', type(v))
print('Só tem espaços? ', v.isspace())
print('É um numero? ', v.isnumeric())
print('É um alfabético? ', v.isalpha())
print('É afanumerico? ', v.isalnum())
print('Estar em maiúsculas? ',v.isupper())
print('Estar em minusculas? ', v.islower())
print('Estar capitalizada? ', v.istitle())


